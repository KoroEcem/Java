package com.ecemkoro.degiskenler;

public class Degiskenler {
  public static void main(String[] args) {

    // İlkel Tipler
    int myInt = 214748364;
    byte myByte = 127;
    long myLong = 2000L;
    short myShort = 20;
    char myChar = 'A';
    double myDouble = 20.0123456789123456d;
    float myFloat = 20.0f;
    boolean myBoolean = true; // false;

    System.out.println("My int: " + myInt);
    System.out.println("My byte: " + myByte);
    System.out.println("My long: " + myLong);
    System.out.println("My short: " + myShort);
    System.out.println("My char: " + myChar);
    System.out.println("My double: " + myDouble);
    System.out.println("My float: " + myFloat);
    System.out.println("My boolean: " + myBoolean);

    System.out.println("-----------------------------");

    // İlkel Olmayan Tipler
    Integer myInteger = 214748364;
    Byte myByte2 = 127;
    Long myLong2 = 2000L;
    Short myShort2 = 20;
    Character myCharacter = 'A';
    Double myDouble2 = 20.0123456789123456d;
    Float myFloat2 = 20.0f;
    Boolean myBoolean2 = true; // false;

    System.out.println("My Integer: " + myInteger);
    System.out.println("My Byte: " + myByte2);
    System.out.println("My Long: " + myLong2);
    System.out.println("My Short: " + myShort2);
    System.out.println("My Char: " + myCharacter);
    System.out.println("My Double: " + myDouble2);
    System.out.println("My Float: " + myFloat2);
    System.out.println("My Boolean: " + myBoolean2);

  }
}
